package crossKT;

public class DefaultValue_Global 
{
	static int a;
	static byte b;
	static double c;
	static boolean d;
	static char e;
	static String f;

	public static void main(String[] args) 
	{
		
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
		System.out.println(e);
		System.out.println(f);
		
	}

}
