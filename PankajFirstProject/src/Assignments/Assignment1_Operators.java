
package Assignments;

public class Assignment1_Operators 
{
	public static void main(String[] args) // add two numbers
	{
		int a=1;
		int b=2;
		int s= a+b;
		System.out.println(s);// Add
		int c=3;
		int d=4;
		int e=d-c;  // Subtract
		System.out.println(e);
		int m=c*d;  // multiply
		System.out.println(m);
		double w=7;
		double r=8;
		double z=r/w; // division
		System.out.println(z);
		
	}
	
	
	
}
