//print even or odd, using for loop
package assignments;

public class Assignment_12 
{

	public static void main(String[] args) 
	{
		for(int i=1;i<=100;i++)
		{
			//System.out.println(i);
			if (i%2==0)
			{
				System.out.println("Even Number:"+ i);
			}
		}
		for(int j=1;j<=100;j++)
		{
			if (j%2==1)
			{
				System.out.println("Odd Number:" + j);
			}
		}
		
			

	}

}
